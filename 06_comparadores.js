// Comparaciones
// Conocemos muchos operadores de comparación de las matemáticas:

// Mayor/menor que: a > b, a < b.
// Mayor/menor o igual que: a >= b, a <= b.

// Igual que: Hay dos tipos
//      1) a == b : Comprara los valores permitiendo la conversión de cada uno.
//          x ej: 0 == false --> true
//      2) a === b : Compara los valores sin permitir conversión (más estricto)
//          x ej: true === 1 --> false

// Distinto: a != b